#include <stdio.h>

#define MOD 12345

int countSequences(int n) {
    int dp[n + 1][2]; // dp[i][j] - кількість послідовностей довжиною i, що закінчуються на j (де j = 0 або 1)

    // Ініціалізація базових значень
    dp[1][0] = 1; // 0
    dp[1][1] = 1; // 1

    // Обчислення кількості послідовностей
    for (int i = 2; i <= n; i++) {
        dp[i][0] = (dp[i - 1][0] + dp[i - 1][1]) % MOD;
        dp[i][1] = dp[i - 1][0];
    }

    // Знаходження загальної кількості послідовностей
    int total = (dp[n][0] + dp[n][1]) % MOD;

    return total;
}

int main() {
    int n;
    printf("Введіть довжину послідовності n: ");
    scanf("%d", &n);

    if (n < 2 || n >= 10000) {
        printf("Некоректний ввід. Довжина послідовності повинна бути в межах 2 і 9999.\n");
        return 1; // Повертаємо 1 для позначення помилки
    }

    int result = countSequences(n);
    printf("Кількість шуканих послідовностей: %d\n", result);

    return 0;
}
